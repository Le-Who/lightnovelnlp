def check_required_env_vars():
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã—Ö –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è"""
    required_vars = [
        'DATABASE_URL',
        'REDIS_URL',
        'GEMINI_API_KEYS_RAW'
    ]
    
    missing_vars = []
    for var in required_vars:
        if not os.getenv(var):
            missing_vars.append(var)
    
    if missing_vars:
        print(f"‚ùå –û—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è: {', '.join(missing_vars)}")
        return False
    
    print("‚úÖ –í—Å–µ –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è –Ω–∞–π–¥–µ–Ω—ã")
    return True

def check_gemini_keys():
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —Ñ–æ—Ä–º–∞—Ç GEMINI_API_KEYS_RAW"""
    keys_str = os.getenv('GEMINI_API_KEYS_RAW', '')
    
    if not keys_str.strip():
        print("‚ùå GEMINI_API_KEYS_RAW –ø—É—Å—Ç–∞—è –∏–ª–∏ –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞")
        return False
    
    # –ü–∞—Ä—Å–∏–º –∫–ª—é—á–∏
    keys = [key.strip() for key in keys_str.split(',') if key.strip()]
    
    if not keys:
        print("‚ùå GEMINI_API_KEYS_RAW –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç –≤–∞–ª–∏–¥–Ω—ã—Ö –∫–ª—é—á–µ–π")
        return False
    
    print(f"‚úÖ –ù–∞–π–¥–µ–Ω–æ {len(keys)} –∫–ª—é—á–µ–π Gemini API")
    return True

def check_database_url():
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç —Ñ–æ—Ä–º–∞—Ç DATABASE_URL"""
    db_url = os.getenv('DATABASE_URL', '')
    
    if not db_url:
        print("‚ùå DATABASE_URL –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞")
        return False
    
    if not db_url.startswith('postgresql://'):
        print("‚ùå DATABASE_URL –¥–æ–ª–∂–Ω–∞ –Ω–∞—á–∏–Ω–∞—Ç—å—Å—è —Å 'postgresql://'")
